package TypeCasting;

public class TypeCastingWrapperClass {
    //wrapper class
    static Byte b1;
    static Short s1;
    static Integer i1;
    static Long l1;
    static Float f1;
    static Double d1;

    public static void main(String[] args) {
        System.out.println(b1);
        System.out.println(s1);
        System.out.println(i1);
        System.out.println(l1);
        System.out.println(f1);
        System.out.println(d1);

//        We cannot Type cast on wrapper class but we can type cast on primitive data types only
//        d1 = 10d;
//        f1 = (Float)d1;



    }
}
